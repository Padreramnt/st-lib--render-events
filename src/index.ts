import * as events from '@st-lib/events'
import { linking } from '@st-lib/render'

function define<A extends any[]>(createEventListener: (...args: A) => (element: EventTarget) => () => void) {
	return (...args: A) => {
		linking(createEventListener(...args))
	}
}

